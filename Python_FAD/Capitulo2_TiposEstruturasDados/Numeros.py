print("Operações Básicas")
print("-----------------")
# Soma
print("Soma: 4 + 4 = ", 4 + 4)
# Subtração
print("Subtração: 4 - 3 = ", 4 - 3)
# Multiplicação
print("Multiplicação: 3 * 3 = ", 3 * 3)
# Divisão
print("Divisão: 3 / 2 = ", 3 / 2)
# Potência
print("Potência: 4 ** 2 = ", 4 ** 2)
# Módulo
print("Módulo: 10 % 3 = ", 10 % 3)

print("\nFunção Type")
print("-----------")
# Função type
print("type(5) = ", type(5))
print("type(5.0) = ", type(5.0))
a = 'Eu sou uma string'
print("type(a) = ", type(a))

print("\nOperações Float")
print("---------------")
# Operações com float
print("3.1 + 6.4 = ", 3.1 + 6.4)
print("4 + 4.0 = ", 4 + 4.0)
print("4 + 4 = ", 4 + 4)
# Resultado é um número float
print("4 / 2 = ", 4 / 2)
# Resultado é um número inteiro
print("4 // 2 = ", 4 // 2)
print("4 / 3.0 = ", 4 / 3.0)
print("4 // 3.0 = ", 4 // 3.0)

print("\nConversão")
print("---------")
print("float(9) = ", float(9))
print("int(6.0) = ", int(6.0))
print("int(6.5) = ", int(6.5))

print("\nHexadecimal e Binário")
print("---------------------")
print("hex(394) = ", hex(394))
print("hex(217) = ", hex(217))
print("bin(286) = ", bin(286))
print("bin(390) = ", bin(390))

print("\nFunções abs, round e pow")
print("------------------------")
# Retorna o valor absoluto
print("abs(-8) = ", abs(-8))
# Retorna o valor absoluto
print("abs(8) = ", abs(8))
# Retorna o valor com arredondamento
print("round(3.14151922, 2) = ", round(3.14151922, 2))
# Potência
print("pow(4, 2) = ", pow(4, 2))
# Potência
print("pow(5 , 3) = ", pow(5, 3))
